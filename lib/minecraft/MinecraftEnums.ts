export type MinecraftAssetType = MinecraftBlockType | MinecraftItemType;

export function getAssetEnum(assetName: string): MinecraftAssetType {
  const enumKey = assetName.toUpperCase() as keyof typeof MinecraftItemType | keyof typeof MinecraftBlockType;

  if (enumKey in MinecraftItemType) {
    return MinecraftItemType[enumKey as keyof typeof MinecraftItemType];
  }

  if (enumKey in MinecraftBlockType) {
    return MinecraftBlockType[enumKey as keyof typeof MinecraftBlockType];
  }

  console.warn(`Unknown asset type: ${assetName}`);
  return MinecraftBlockType.BEDROCK;
}

export function getAssetPath(assetType: MinecraftAssetType): string {
  if (Object.values(MinecraftBlockType).includes(assetType as MinecraftBlockType)) {
    return `/minecraft/block/${assetType}.png`;
  } else if (Object.values(MinecraftItemType).includes(assetType as MinecraftItemType)) {
    return `/minecraft/item/${assetType}.png`;
  } else {
    throw new Error(`Invalid asset type: ${assetType}`);
  }
}
